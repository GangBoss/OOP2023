using System;
using System.Collections.Generic;
using System.Linq;
using System.Security.Cryptography;
using System.Text;
using System.Threading.Tasks;
using System.Xml.Linq;
using NUnit.Framework;

namespace ObjectPrinting.Tests
{
    [TestFixture]
    public class ObjectPrinterAcceptanceTests
    {
        [Test]
        public void Demo()
        {
            var person = new Person { Name = "Alex", Age = 19 };

            var printer = ObjectPrinter.For<Person>();
            //1. Исключить из сериализации свойства определенного типа
            //2. Указать альтернативный способ сериализации для определенного типа
            //3. Для числовых типов указать культуру
            //4. Настроить сериализацию конкретного свойства
            //5. Настроить обрезание строковых свойств (метод должен быть виден только для строковых свойств)
            //6. Исключить из сериализации конкретного свойства

            string s1 = printer.PrintToString(person);
            Assert.AreEqual("System.Int32 Age - 19System.String Name - Alex",s1);
            //1
            var s2 = printer.ExcludeType(typeof(int)).PrintToString(person);
            Assert.AreEqual("System.String Name - Alex", s2);
            //2
            Func<Person, string> serializer = (obj) => "try";
            var s3 = printer.ExcludeType(typeof(int)).SetDifferentSerializer(serializer).PrintToString(person);
            Assert.AreEqual("try", s3);

            //7. Синтаксический сахар в виде метода расширения, сериализующего по-умолчанию        
            //8. ...с конфигурированием
        }
    }
}
